# set: set is a data type used in python to store the unique values 
# set is unordered collection of elements 
# set is initialize by {} curly braces

# initializing set
s = {1,2,3,4,5}
print(type(s))


# if you want to create a empty set, then you have to use set() constructor
s2 = set()
print(type(s2))


# if we create an empty set, then it gives dictionary data type
s1 ={}
print(type(s1))


# the set stores the unique elements 
s = {1,2,3,4,1,2,3}
